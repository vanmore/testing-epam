package by.van.common;

public class FlightData {
    private String flightStartingPoint;
    private String flightDestinationPoint;

    public String getFlightStartingPoint() {
        return flightStartingPoint;
    }

    public void setFlightStartingPoint(String flightStartingPoint) {
        this.flightStartingPoint = flightStartingPoint;
    }

    public String getFlightDestinationPoint() {
        return flightDestinationPoint;
    }

    public void setFlightDestinationPoint(String flightDestinationPoint) {
        this.flightDestinationPoint = flightDestinationPoint;
    }
}
